const selectTag = document.querySelectorAll("select");
const icons = document.querySelectorAll(".row i");
const fromText = document.querySelector(".from-text");
const toText = document.querySelector(".to-text");
const exchangeIcon = document.querySelector(".exchange");
const translateBtn = document.querySelector("button");

selectTag.forEach((selectTag, id) => {
  for (const country_code in countries) {
    let selected =
      (id == 0 && country_code == "en-GB") ||
      (id == 1 && country_code == "hi-IN")
        ? "selected"
        : "";
    let option = `<option value="${country_code}" ${selected}>${countries[country_code]}</option>`;
    selectTag.insertAdjacentHTML("beforeend", option);
  }
});

translateBtn.addEventListener("click", () => {
  let text = fromText.value;
  let translateFrom = selectTag[0].value;
  let translateTo = selectTag[1].value;

  if (!text) {
    alert("Please enter text to translate.");
    return;
  }

  toText.setAttribute("placeholder", "Translating...");

  let url = `https://api.mymemory.translated.net/get?q=${text}&langpair=${translateFrom}|${translateTo}`;
  fetch(url)
    .then((res) => res.json())
    .then((data) => {
      if (data.responseStatus === 200) {
        toText.value = data.responseData.translatedText;
      } else {
        alert("Translation failed. Please try again.");
      }
    })
    .catch((error) => {
      alert("An error occurred. Please try again later.");
      toText.removeAttribute("placeholder");
    });
});

exchangeIcon.addEventListener("click", () => {
  let tempText = fromText.value,
    tempLang = selectTag[0].value;
  fromText.value = toText.value;
  selectTag[0].value = selectTag[1].value;
  toText.value = tempText;
  selectTag[1].value = tempLang;
});

icons.forEach((icon) => {
  icon.addEventListener("click", ({ target }) => {
    if (target.classList.contains("fa-copy")) {
      const tooltip = document.querySelector(".tooltip");
      tooltip.classList.add("show-tooltip");
      setTimeout(() => {
        tooltip.classList.remove("show-tooltip");
      }, 1000);
      if (target.id == "from") {
        navigator.clipboard.writeText(fromText.value);
      } else {
        navigator.clipboard.writeText(toText.value);
      }
    } else {
      let utterance;
      if (target.id == "from") {
        utterance = new SpeechSynthesisUtterance(fromText.value);
        utterance.lang = selectTag[0].value;
      } else {
        utterance = new SpeechSynthesisUtterance(toText.value);
        utterance.lang = selectTag[1].value;
      }
      speechSynthesis.speak(utterance);
    }
  });
});
